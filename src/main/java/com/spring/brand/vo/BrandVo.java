package com.spring.brand.vo;

public class BrandVo {

	private String br_Init;
	private String br_Id;
	private String br_Name;
	private String br_Detail;
	
	public String getBr_Init() {
		return br_Init;
	}
	public void setBr_Init(String br_Init) {
		this.br_Init = br_Init;
	}
	public String getBr_Id() {
		return br_Id;
	}
	public void setBr_Id(String br_Id) {
		this.br_Id = br_Id;
	}
	public String getBr_Name() {
		return br_Name;
	}
	public void setBr_Name(String br_Name) {
		this.br_Name = br_Name;
	}
	public String getBr_Detail() {
		return br_Detail;
	}
	public void setBr_Detail(String br_Detail) {
		this.br_Detail = br_Detail;
	}
	
	
}
